package com.cec.tv.model;

public class Manage {
    private String manage_name;
    private String manage_image;
    private String manage_id;

    public String getManage_name() {
        return manage_name;
    }

    public void setManage_name(String manage_name) {
        this.manage_name = manage_name;
    }

    public String getManage_image() {
        return manage_image;
    }

    public void setManage_image(String manage_image) {
        this.manage_image = manage_image;
    }

    public String getManage_id() {
        return manage_id;
    }

    public void setManage_id(String manage_id) {
        this.manage_id = manage_id;
    }
}
